'use strict';
Object.defineProperties(Date.prototype,{
	UY:{get(){return this.getUTCFullYear();}, set(v){this.setUTCFullYear(v);}},
	Y:{get(){return this.getFullYear();}, set(v){this.setFullYear(v);}},
	M:{get(){return this.getMonth()+1;}, set(v){this.setMonth(v-1);}},
	D:{get(){return this.getDate();}, set(v){this.setDate(v);}},
	h:{get(){return this.getHours();}, set(v){this.setHours(v);}},
	m:{get(){return this.getMinutes();}, set(v){this.setMinutes(v);}},
	s:{get(){return this.getSeconds();}, set(v){this.setSeconds(v);}},
	ms:{get(){return this.getMilliseconds();}, set(v){this.setMilliseconds(v)}},
	d:{get(){return this.getDay();}, set(v){this.setDay(v);}},
	UM:{get(){return this.getUTCMonth()+1;}, set(v){this.setUTCMonth(v-1);}},
	UD:{get(){return this.getUTCDate();}, set(v){this.setUTCDate(v);}},
	Uh:{get(){return this.getUTCHours();}, set(v){this.setUTCHours(v);}},
	Um:{get(){return this.getUTCMinutes();}, set(v){this.setUTCMinutes(v);}},
	Us:{get(){return this.getUTCSeconds();}, set(v){this.setUTCSeconds(v);}},
	Ums:{get(){return this.getUTCMilliseconds();}, set(v){this.setUTCMilliseconds(v)}},
	Ud:{get(){return this.getUTCDay();}, set(v){this.setUTCDay(v);}},
	Uy:{get(){return this.getYear();}},
	tz:{get(){return this.getTimezoneOffset();}},
	ISO:{get(){return this.toISOString();}},
	GMT:{get(){return this.toGMTString();}},
	UTC:{get(){return this.toUTCString();}},
	time:{get(){return this.toTimeString();}},
	date:{get(){return this.toDateString();}},
	locale:{get(){return this.toLocaleString();}},
	localeTime:{get(){return this.toLocaleTimeString();}},
	localeDate:{get(){return this.toLocaleDateString();}},
	two:{value:function(v){return String(v).padStart(2,'0');}},
	normal:{get(){return this.Y+'.'+this.two(this.M)+'.'+this.two(this.D)+' '+this.two(this.h)+':'+this.two(this.m)+':'+this.two(this.s); }}
});